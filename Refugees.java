public class Refugees {
int Year;
String Country;
double Population;
double Refugees;
double RefPerCap;
double TotalPopulation;
double TotalRefugees;

    public double getTotalRefugees() {
        return TotalRefugees;
    }

    public void setTotalRefugees(double totalRefugees) {
        TotalRefugees = totalRefugees;
    }

    public double getTotalPopulation() {
        return TotalPopulation;
    }

    public void setTotalPopulation(double totalPopulation) {
        TotalPopulation = totalPopulation;
    }

    public int getYear() {
        return Year;
    }

    public void setYear(int year) {
        Year = year;
    }

    public String getCountry() {
        return Country;
    }

    public void setCountry(String country) {
        Country = country;
    }

    public double getPopulation() {
        return Population;
    }

    public void setPopulation(double population) {
        Population = population;
    }

    public double getRefugees() {
        return Refugees;
    }

    public void setRefugees(double refugees) {
        Refugees = refugees;
    }

    public double getRefPerCap() {
        return RefPerCap;
    }

    public void setRefPerCap(double refPerCap) {
        RefPerCap = refPerCap;
    }
}
