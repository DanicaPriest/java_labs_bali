package part_05.Exercise_02;

public class Angora {
    private int fluffLevel;
    private String color;
    private String name;
    private boolean isFluffyEnough;

    public int getFluffLevel() {
        return fluffLevel;
    }

    public void setFluffLevel(int fluffLevel) {
        this.fluffLevel = fluffLevel;

    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isFluffyEnough() {
        return isFluffyEnough;
    }

    public void setFluffyEnough(boolean fluffyEnough) {
        isFluffyEnough = fluffyEnough;
    }
}
